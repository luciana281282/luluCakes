  function Boda() {
    dropdownBtn.textContent = "Boda";
    breadcrumb.textContent = "Boda";
  
  for (let i=0;i<8;i++) {
    
    let Name = "productName" + i
    let idName = Name.toString()
    document.getElementById(idName).innerHTML = boda[i].nombreProducto;
  
    let description = "productDescription" + i
    let idDescription = description.toString()
    document.getElementById(idDescription).innerHTML = boda[i].descripcionProducto;
  
    let background = "card" + i
    let idBackground = background.toString()
    document.getElementById(idBackground).style.background = boda[i].background
    document.getElementById(idBackground).style.backgroundSize = "cover"
  }  
  }
  
